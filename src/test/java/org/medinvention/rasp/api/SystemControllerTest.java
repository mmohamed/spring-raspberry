package org.medinvention.rasp.api;

import static org.hamcrest.Matchers.is;
import static org.springframework.test.web.servlet.request.MockMvcRequestBuilders.get;
import static org.springframework.test.web.servlet.result.MockMvcResultMatchers.jsonPath;
import static org.springframework.test.web.servlet.result.MockMvcResultMatchers.status;

import org.junit.Test;
import org.medinvention.rasp.BaseControllerTestUnit;
import org.springframework.http.MediaType;

public class SystemControllerTest extends BaseControllerTestUnit {

    @Test
    public void testStatusGet() throws Exception {
        this.mvc.perform(get("/api/system/status").accept(MediaType.APPLICATION_JSON_UTF8)).andExpect(status().isOk())
                .andExpect(jsonPath("$.platform.id", is("raspberrypi")));
    }
}
